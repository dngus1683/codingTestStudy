void refunc(int cnt, string line)
{
	cout << line << answer[0] <<'\n';
	if (cnt == N)
	{
		cout << line << answer[4] << '\n';
	}
	else
	{
		cout << line << answer[1] << '\n';
		cout << line << answer[2] << '\n';
		cout << line << answer[3] << '\n';
		refunc(cnt + 1, line + "----");
	}
	cout << line << answer[5] << '\n';
}


